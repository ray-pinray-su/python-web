#######################匯入模組#######################
from tkinter import *

#######################建立視窗########################
# 創建主視窗
window = Tk()
# 設定主視窗標題
window.title("my first GUI")
######################創建畫布#########################
# 創建畫布，並指定畫布的寬度為600像素，高度為600像素，背景色為白色
canvas = Canvas(window, width=600, height=600, bg="white")
# 將畫布放置在視窗中
canvas.pack()

#######################運行應用程式########################
window.mainloop()
